

function A() {
    document.getElementById("SA").play();
    document.getElementById("SecE").style.opacity = '0%';
    document.getElementById("SecI").style.opacity = '0%';
    document.getElementById("SecO").style.opacity = '0%';
    document.getElementById("SecU").style.opacity = '0%';
    setTimeout( function(){
        document.getElementById("SecE").style.display = 'none';
        document.getElementById("SecI").style.display = 'none';
        document.getElementById("SecO").style.display = 'none';
        document.getElementById("SecU").style.display = 'none';
        document.getElementById("SecA").style.display = 'block';
        document.getElementById("SecA").style.opacity = '100%';
    },1000)
}
